/**
 * 写一个程序，打印0-100的值
 */
public class Practice1 {

    public static void printNum(){
        for (int i = 0; i <= 100; i++) {
            System.out.println(i);
        }
    }
    public static void main(String[] args){
      printNum();
    }
}
